        static public void teamInfo()
        {
            List<Team> useage = new List<Team>(teamData);
            List<int> money1 = new List<int>() {79465000, 39625000, 38431000, 29092000, 80603000, 31497000, 53573000, 16641000, 16501000, 43796000, 85804000, 185584000, 186104000, 33224000, 22145000, 33608000, 78165000, 18361000, 39680000, 44802000};
            List<int> money2 = new List<int>() {3500000, 5167000, 7670000, 33212000, 11125000, 10900000, 8880000, 2078000, 9244000, 6000000, 2316000, 8750000, 3816000, 21241000, 16536000, 3567000, 3563000, 4800000, 2005000, 5062000, 8264000, 10230000, 36002000, 1574000};
            List<int> money3 = new List<int>() {1503000, 1544000, 1710000, 1728000, 1625000, 2863000, 1253000, 1960000, 1869000, 1782000, 4245000, 2632000, 1938000, 1922000, 1710000, 1908000, 2105000, 1846000, 2072000, 1550000, 1616000, 4875000, 1801000, 2358000};
            int thisMoney;
            int thisTeam;
            for (int thisDiv = 1; thisDiv < 4; thisDiv++)
            {
                for (int twentyTeams = 0; twentyTeams < 20; twentyTeams++)
                {
                    thisTeam = Program.rnd.Next(useage.ToArray().Length);
                    useage[thisTeam].div = thisDiv;
                    if(thisDiv == 1)
                    {
                        thisMoney = Program.rnd.Next(money1.ToArray().Length);
                        useage[thisTeam].currency = money1[thisMoney];
                        money1.RemoveAt(thisMoney);
                    }
                    else if(thisDiv == 2)
                    {
                        thisMoney = Program.rnd.Next(money2.ToArray().Length);
                        useage[thisTeam].currency = money2[thisMoney];
                        money2.RemoveAt(thisMoney);
                    }
                    else
                    {
                        thisMoney = Program.rnd.Next(money3.ToArray().Length);
                        useage[thisTeam].currency = money3[thisMoney];
                        money3.RemoveAt(thisMoney);
                    }
                    for (int i = 0; i < teamData.ToArray().Length; i++)
                    {
                        if (useage[thisTeam].teamName.Equals(teamData[i].teamName))
                        {
                            teamData[i].div = useage[thisTeam].div;
                            teamData[i].currency = useage[thisTeam].currency;
                            League.leagueData[(thisDiv-1)].divTeam.Add(teamData[i]);
                        }
                    }
                    useage.RemoveAt(thisTeam);
                }
            }
        }
